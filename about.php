              <p>Layanan Survei dan Aduan Masyarakat adalah layanan yang kami sediakan sebagai wujud pengembangan dan peningkatan layanan terhadap masyarakat umum maupun mitra BLU P3GL dengan 2 sistem yang ada, yaitu: sistem pengaduan wistleblowing untuk umum, dan sistem survei untuk mitra BLU P3GL. Sistem pengaduan wistleblowing dibuat untuk melaporkan kemungkinan kecurangan, aktivitas yang tidak jujur atau kecurangan lain yang dilakukan oleh pegawai P3GL di tempat kerja ataupun di luar tempat kerja dengan beridentitas sebagai pegawai P3GL. Sedangkan sistem survei dibuat untuk mengumpulkan data dari mitra-mitra BLU P3GL untuk selalu memperbaiki layanan demi kepentingan bersama. </p>
